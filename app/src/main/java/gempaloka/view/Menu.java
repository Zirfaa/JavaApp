    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed

                String nama = tfnama.getText().trim();
                String alamat = tfalamat.getText().trim();
                String provinsi = tfprovinsi.getText().trim();
            if (!nama.isEmpty() && !alamat.isEmpty()) {
                TempatEvakuasiController controller = new TempatEvakuasiController();
                controller.tambah(nama, alamat,provinsi);
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
                tfnama.setText("");
                tfalamat.setText("");

            } else {
                JOptionPane.showMessageDialog(this, "Isi semua field!");
            }
    }//GEN-LAST:event_btntambahActionPerformed

